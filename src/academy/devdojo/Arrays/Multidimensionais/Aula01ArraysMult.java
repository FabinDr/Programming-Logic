package academy.devdojo.Arrays.Multidimensionais;

public class Aula01ArraysMult {
    public static void main(String[] args) {
        int[][] ArraysMult = new int[2][2];
        ArraysMult[0][0] = 22;
        ArraysMult[0][1] = 11;
        ArraysMult[1][0] = 44;
        ArraysMult[1][1] = 55;
        System.out.println(ArraysMult[0][0]);
        System.out.println(ArraysMult[0][1]);
        System.out.println(ArraysMult[1][0]);
        System.out.println(ArraysMult[1][1]);
    }
}
